s1 = "aaabbb"
s2 = "aba"
s3 = "ababab"
s4 = "abcbabc"


class Solution:
    def strangePrinter(self, s: str) -> int:

        if len(s) == 0:
            return 0
        
        


s = Solution()
print(s.strangePrinter(s1))
print(s.strangePrinter(s2))
print(s.strangePrinter(s3))
print(s.strangePrinter(s4))
